/** @format */

import Head from "next/head";
import Image from "next/image";
import styles from "../styles/Home.module.scss";

import HeroImage from "../public/static/hero-image.png";
import Banner from "../components/Banner";
import Card from "../components/Card";
import { fetchCoffeeStores } from "../lib/coffee-stores";

export const getStaticProps = async () => {
  try {
    const coffeeStores = await fetchCoffeeStores();

    return {
      props: {
        coffeeStores,
      }, // will be passed to the page component as props
    };
  } catch (error) {
    console.log(error);
  }
};

const handleOnBannerButtonClick = () => console.log("Hi Banner button");

export default function Home({ coffeeStores: cs }) {
  return (
    <div className={styles.container}>
      <Head>
        <title>Coffee Connoisseur</title>
        <meta name='description' content='Generated by create next app' />
        <link rel='icon' href='/favicon.ico' />
      </Head>

      <main className={styles.main}>
        <Banner
          buttonText={"View stores nearby"}
          handleOnClick={handleOnBannerButtonClick}
        />
        <div className={styles.heroImage}>
          <Image src={HeroImage} alt='' width={1200} height={400} />
        </div>

        {cs.length > 0 && <h2 className={styles.heading2}>Toronto stores</h2>}

        <div className={styles.cardLayout}>
          {cs.map((coffeeStore) => {
            return (
              <Card
                key={coffeeStore.fsq_id}
                className={styles.card}
                name={coffeeStore.name}
                href={`/coffee-store/${coffeeStore.fsq_id}`}
                imgUrl={
                  coffeeStore.imgUrl ||
                  "https://images.unsplash.com/photo-1504753793650-d4a2b783c15e?ixid=MnwxMjA3fDB8MHxwaG90by1wYWdlfHx8fGVufDB8fHx8&ixlib=rb-1.2.1&auto=format&fit=crop&w=2000&q=80"
                }
              />
            );
          })}
        </div>
      </main>
    </div>
  );
}
